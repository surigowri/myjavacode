//Swap two numbers with extra variable

public class Gowri_Assignment_53 
{

	public static void main(String[] args) 
	{

		int x = 40;
		int y = 60;
		int z = 0;
		
		z = x; // x=0, z=40
		x = y; // y=0, x=60
		y = z; //y=40, z=0
		
		System.out.println(x);
		System.out.println(y);
	}

}
